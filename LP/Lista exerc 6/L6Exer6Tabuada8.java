/*
6) Escreva um programa em Java que calcule e imprima a 
* tabuada do 8 (1 a 10). 
 */


public class L6Exer6Tabuada8 {
	
	public static void main (String[] args) {
		
		int numero = 0;
		int tabuada = 8;
		
		//for (incializacao ; teste logico ; incr/dec)
		for (numero = 1 ; numero > 0  ;  numero ++ ) {
			
			tabuada = (numero * 8) ;
			
			System.out.print ("Tabuada do 8: ");
	}
}

}
